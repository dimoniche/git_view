#include "ui/EditorTheme.h"

#include <QPalette>
#include <QWidget>

bool editorUsesDarkTheme(const QWidget *widget)
{
    if (!widget) {
        return false;
    }
    const QPalette palette = widget->palette();
    return palette.color(QPalette::Text).lightness() > palette.color(QPalette::Base).lightness();
}

ChangeHighlightTheme removedChangeTheme(bool dark)
{
    if (dark) {
        return {QColor(0x4d, 0x22, 0x22), QColor(0xf0, 0xd8, 0xd8), QColor(0xff, 0xa0, 0xa0),
                QColor(0xff, 0xb8, 0xb8), QColor(0xcc, 0x99, 0x99), QColor(0xff, 0xc0, 0x90),
                QColor(0xdd, 0xb8, 0x96)};
    }
    return {QColor(0xff, 0xb8, 0xb8), QColor(0x1a, 0x1a, 0x1a), QColor(0x7a, 0x00, 0x00),
            QColor(0x8b, 0x00, 0x00), QColor(0x66, 0x33, 0x33), QColor(0x7a, 0x2e, 0x00),
            QColor(0x5c, 0x3d, 0x1a)};
}

ChangeHighlightTheme addedChangeTheme(bool dark)
{
    if (dark) {
        return {QColor(0x22, 0x4d, 0x22), QColor(0xd8, 0xf0, 0xd8), QColor(0xa0, 0xff, 0xa0),
                QColor(0xb8, 0xff, 0xb8), QColor(0x99, 0xcc, 0x99), QColor(0x90, 0xff, 0xc0),
                QColor(0xb6, 0xdd, 0x96)};
    }
    return {QColor(0xb8, 0xff, 0xb8), QColor(0x1a, 0x1a, 0x1a), QColor(0x00, 0x5a, 0x00),
            QColor(0x00, 0x6b, 0x00), QColor(0x33, 0x66, 0x33), QColor(0x00, 0x5c, 0x45),
            QColor(0x3d, 0x5c, 0x1a)};
}

QColor paddingLineBackground(bool dark)
{
    return dark ? QColor(0x3a, 0x3a, 0x3a) : QColor(0xe4, 0xe4, 0xe4);
}

DiffLineTheme diffAddedLineTheme(bool dark)
{
    if (dark) {
        return {QColor(0xa8, 0xff, 0xb8), QColor(0x1e, 0x4d, 0x28)};
    }
    return {QColor(0x00, 0x55, 0x00), QColor(0xb8, 0xff, 0xb8)};
}

DiffLineTheme diffRemovedLineTheme(bool dark)
{
    if (dark) {
        return {QColor(0xff, 0xb0, 0xb0), QColor(0x4d, 0x1e, 0x1e)};
    }
    return {QColor(0x7a, 0x00, 0x00), QColor(0xff, 0xb8, 0xb8)};
}
